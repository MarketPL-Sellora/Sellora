import axios, { type AxiosInstance } from 'axios'

// Базова URL-адреса бекенду. Всі запити через apiClient матимуть цей префікс.
// Змінити на реальний URL після деплою бекенду.
const API_URL = 'http://localhost:3000/api' // Тимчасовий локальний бекенд

// Створюємо єдиний екземпляр Axios для всього застосунку.
// Використання одного екземпляру (singleton) дозволяє централізовано
// керувати заголовками, інтерсепторами та базовим URL.
export const apiClient: AxiosInstance = axios.create({
  baseURL: API_URL,

  // Максимальний час очікування відповіді від сервера — 10 секунд.
  // Якщо сервер не відповів за цей час, запит автоматично скасовується з помилкою.
  timeout: 10000,

  headers: {
    // Повідомляємо серверу, що відправляємо та очікуємо отримати JSON.
    "Content-Type": "application/json",
  },

  // КЛЮЧОВА ОПЦІЯ для HttpOnly Cookies:
  // withCredentials: true — дозволяє браузеру автоматично прикріплювати
  // та зберігати HttpOnly cookie під час крос-доменних запитів.
  // Без цього прапорця бекенд не зможе встановити сесійний cookie,
  // і авторизація через cookie не працюватиме.
  withCredentials: true,
})
